#include "../push_swap.h"

int	isall_digit(char **av, int ac)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (ac > 0)
	{
		while (av[i][j])
		{
			if (av[i][j] < '0' && av[i][j] > '9')
				return (0);
			j++;
		}
		i++;
		ac--;
	}
	return (1);
}